public class App {
    public static void main(String[] args) {

        int numeroUm = 40;
        int numeroDois = 20;

        int soma = numeroUm + numeroDois;
        System.out.println("A soma: " + soma);
        System.out.println("/* --------- */");

        int subtracao = numeroUm - numeroDois;
        System.out.println("A subtracao: " + subtracao);
        System.out.println("/* --------- */");

        int divisao = numeroUm / numeroDois;
        System.out.println("A divisao: " + divisao);
        System.out.println("/* --------- */");

        int multiplicacao = numeroUm * numeroDois;
        System.out.println("A multiplicação: " + multiplicacao);

    }
}
